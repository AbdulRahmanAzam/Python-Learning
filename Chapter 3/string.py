name = "ABDUL RAHMAN AZAM"

# in PYTHON there are two types of indexing 
# one starts from the beginning with 0 
# other starts from the end with -1


start3 = name[0 : 3] # 0 till 3 except 3
print(start3) # ABD

innegative = name[-8 : -1]
print(innegative)

print(name[ : 4]) # means 0 is written in first // same as name[0:4]

print(name[4 : ]) # means n-1 is written in second // same as name[4:n-1]